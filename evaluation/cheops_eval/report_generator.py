"""
Publication-quality report generator for CHEOPS 2026.

Produces:
  • LaTeX tables (ACM / EuroSys compatible)
  • matplotlib figures (3.3 in column-width, PDF vector)
  • Plain-text summary
  • JSON data dump

All outputs land in  evaluation_results/cheops/
"""

from __future__ import annotations

import json
import logging
import os
import statistics
from dataclasses import asdict
from datetime import datetime
from pathlib import Path
from typing import Any, Dict, List, Optional

logger = logging.getLogger(__name__)

# Lazy-import matplotlib so the module can be imported without a display
_MPL_AVAILABLE = False
try:
    import matplotlib
    matplotlib.use("Agg")
    import matplotlib.pyplot as plt
    import matplotlib.ticker as mticker
    _MPL_AVAILABLE = True
except ImportError:
    pass

# ── style constants for ACM column figures ───────────────────────────────
COL_WIDTH = 3.33            # inches  (ACM single-column)
FULL_WIDTH = 7.0            # inches  (ACM double-column)
DPI = 300
FONT_SIZE = 8
FONT_FAMILY = "serif"


def _apply_style():
    if not _MPL_AVAILABLE:
        return
    plt.rcParams.update({
        "font.family": FONT_FAMILY,
        "font.size": FONT_SIZE,
        "axes.labelsize": FONT_SIZE,
        "axes.titlesize": FONT_SIZE + 1,
        "xtick.labelsize": FONT_SIZE - 1,
        "ytick.labelsize": FONT_SIZE - 1,
        "legend.fontsize": FONT_SIZE - 1,
        "figure.dpi": DPI,
        "savefig.dpi": DPI,
        "savefig.bbox": "tight",
        "savefig.pad_inches": 0.02,
    })


class ReportGenerator:
    """Turn evaluation report dataclasses into publication artifacts."""

    def __init__(self, output_dir: str = "./evaluation_results/cheops"):
        self.out = Path(output_dir)
        self.out.mkdir(parents=True, exist_ok=True)
        self.ts = datetime.now().strftime("%Y%m%d_%H%M%S")
        _apply_style()

    # ── public entry point ───────────────────────────────────────────

    def generate(
        self,
        intent_report=None,
        react_report=None,
        safety_report=None,
        anomaly_report=None,
        latency_report=None,
    ):
        """Generate all artifacts from whichever reports are provided."""
        # JSON dump
        bundle = {}
        if intent_report:
            bundle["intent"] = self._serialise(intent_report)
        if react_report:
            bundle["react"] = self._serialise(react_report)
        if safety_report:
            bundle["safety"] = self._serialise(safety_report)
        if anomaly_report:
            bundle["anomaly"] = self._serialise(anomaly_report)
        if latency_report:
            bundle["latency"] = self._serialise(latency_report)

        json_path = self.out / f"cheops_eval_{self.ts}.json"
        with open(json_path, "w") as f:
            json.dump(bundle, f, indent=2, default=str)
        logger.info("Saved JSON: %s", json_path)

        # LaTeX
        latex = self._build_latex(
            intent_report, react_report, safety_report,
            anomaly_report, latency_report,
        )
        tex_path = self.out / f"tables_{self.ts}.tex"
        with open(tex_path, "w") as f:
            f.write(latex)
        logger.info("Saved LaTeX: %s", tex_path)

        # Figures
        if _MPL_AVAILABLE:
            self._make_figures(
                intent_report, react_report, safety_report,
                anomaly_report, latency_report,
            )

        # Text summary
        txt = self._text_summary(
            intent_report, react_report, safety_report,
            anomaly_report, latency_report,
        )
        txt_path = self.out / f"summary_{self.ts}.txt"
        with open(txt_path, "w") as f:
            f.write(txt)
        logger.info("Saved summary: %s", txt_path)

        return {
            "json": str(json_path),
            "latex": str(tex_path),
            "text": str(txt_path),
        }

    # ── serialisation helper ─────────────────────────────────────────

    @staticmethod
    def _serialise(obj) -> Any:
        if hasattr(obj, "__dataclass_fields__"):
            return asdict(obj)
        if hasattr(obj, "dict"):
            return obj.dict()
        return obj

    # ==================================================================
    #  LaTeX tables
    # ==================================================================

    def _build_latex(self, intent, react, safety, anomaly, latency) -> str:
        parts: List[str] = [
            "% Auto-generated by Ceph-SRE evaluation suite",
            f"% Timestamp: {self.ts}",
            "",
        ]

        if intent:
            parts.append(self._latex_intent(intent))
        if react:
            parts.append(self._latex_react(react))
        if safety:
            parts.append(self._latex_safety(safety))
        if anomaly:
            parts.append(self._latex_anomaly(anomaly))
        if latency:
            parts.append(self._latex_latency(latency))
            if latency.cli_profiles:
                parts.append(self._latex_cli(latency))

        return "\n".join(parts)

    # ── Table 1: Intent classification ───────────────────────────────

    def _latex_intent(self, rpt) -> str:
        rows = []
        for cat, d in sorted(rpt.category_accuracy.items()):
            m = d["mean"] if isinstance(d, dict) else d
            s = d.get("std", 0) if isinstance(d, dict) else 0
            rows.append(
                f"  {cat.replace('_', ' ').title()} & "
                f"${m:.1f} \\pm {s:.1f}$ \\\\"
            )
        cat_rows = "\n".join(rows)

        return rf"""
% Table 1: Intent Classification Accuracy
\begin{{table}}[t]
\centering
\caption{{Intent classification accuracy (mean $\pm$ std, N={rpt.num_runs} runs,
  {rpt.num_tests_per_run} tests, model: \texttt{{{rpt.model}}}).}}
\label{{tab:intent}}
\begin{{tabular}}{{lr}}
\toprule
\textbf{{Metric}} & \textbf{{Result (\%)}} \\
\midrule
Intent Accuracy & ${rpt.intent_accuracy_mean:.1f} \pm {rpt.intent_accuracy_std:.1f}$ \\
Parameter Accuracy & ${rpt.param_accuracy_mean:.1f} \pm {rpt.param_accuracy_std:.1f}$ \\
\midrule
\multicolumn{{2}}{{l}}{{\textbf{{Per-Category Accuracy}}}} \\
\midrule
{cat_rows}
\bottomrule
\end{{tabular}}
\end{{table}}
"""

    # ── Table 2: ReAct vs Simple ─────────────────────────────────────

    def _latex_react(self, rpt) -> str:
        return rf"""
% Table 2: ReAct vs Simple Mode Comparison
\begin{{table}}[t]
\centering
\caption{{ReAct agent loop vs.\ simple intent-execute pipeline
  ({rpt.num_tests} scenarios).}}
\label{{tab:react}}
\begin{{tabular}}{{lrr}}
\toprule
\textbf{{Metric}} & \textbf{{Simple}} & \textbf{{ReAct}} \\
\midrule
Task Success Rate (\%) & {rpt.simple_success_rate:.1f} & {rpt.react_success_rate:.1f} \\
Avg Latency (ms) & {rpt.simple_avg_latency_ms:.0f} & {rpt.react_avg_latency_ms:.0f} \\
Avg Steps & 1 & {rpt.react_avg_steps:.1f} \\
Tool Coverage & --- & {rpt.react_tool_coverage:.0%} \\
\midrule
Routing Accuracy (\%) & \multicolumn{{2}}{{c}}{{{rpt.routing_accuracy:.1f}}} \\
\midrule
ReAct Wins / Simple Wins / Ties & \multicolumn{{2}}{{c}}{{{rpt.react_wins} / {rpt.simple_wins} / {rpt.ties}}} \\
\bottomrule
\end{{tabular}}
\end{{table}}
"""

    # ── Table 3: Safety ──────────────────────────────────────────────

    def _latex_safety(self, rpt) -> str:
        level_rows = []
        for lv in ["LOW", "MEDIUM", "HIGH", "CRITICAL"]:
            acc = rpt.per_level_accuracy.get(lv, 0)
            level_rows.append(f"  {lv} & {acc:.0f} \\\\")
        lr = "\n".join(level_rows)

        return rf"""
% Table 3: Safety Framework Evaluation
\begin{{table}}[t]
\centering
\caption{{Action-engine risk-classification accuracy ({rpt.num_tests} actions).}}
\label{{tab:safety}}
\begin{{tabular}}{{lr}}
\toprule
\textbf{{Metric}} & \textbf{{Accuracy (\%)}} \\
\midrule
Overall Risk Classification & {rpt.risk_accuracy:.1f} \\
Confirmation Gate Accuracy & {rpt.confirmation_accuracy:.1f} \\
\midrule
\multicolumn{{2}}{{l}}{{\textbf{{Per-Risk-Level}}}} \\
\midrule
{lr}
\bottomrule
\end{{tabular}}
\end{{table}}
"""

    # ── Table 4: Anomaly Detection ───────────────────────────────────

    def _latex_anomaly(self, rpt) -> str:
        cat_rows = []
        for cat, d in sorted(rpt.per_category_detection.items()):
            tp = d.get("tp", 0)
            fp = d.get("fp", 0)
            fn = d.get("fn", 0)
            prec = tp / (tp + fp) if (tp + fp) > 0 else 0
            rec = tp / (tp + fn) if (tp + fn) > 0 else 0
            cat_rows.append(
                f"  {cat} & {tp} & {fp} & {fn} & "
                f"{prec:.2f} & {rec:.2f} \\\\"
            )
        cr = "\n".join(cat_rows)

        return rf"""
% Table 4: Anomaly Detection
\begin{{table}}[t]
\centering
\caption{{Anomaly detection evaluation ({rpt.num_scenarios} synthetic scenarios).}}
\label{{tab:anomaly}}
\begin{{tabular}}{{lrrrrr}}
\toprule
\textbf{{Category}} & \textbf{{TP}} & \textbf{{FP}} & \textbf{{FN}} &
  \textbf{{Prec.}} & \textbf{{Recall}} \\
\midrule
{cr}
\midrule
\textbf{{Macro Avg}} & & & & {rpt.macro_precision:.2f} & {rpt.macro_recall:.2f} \\
\textbf{{Score Accuracy}} & \multicolumn{{5}}{{c}}{{{rpt.score_accuracy:.1f}\%}} \\
\textbf{{F1}} & \multicolumn{{5}}{{c}}{{{rpt.f1_score:.3f}}} \\
\bottomrule
\end{{tabular}}
\end{{table}}
"""

    # ── Table 5: Latency decomposition ───────────────────────────────

    def _latex_latency(self, rpt) -> str:
        rows = []
        for p in rpt.profiles:
            rows.append(
                f"  {p.label.replace('_', ' ').title()} & "
                f"${p.total_mean:.0f} \\pm {p.total_std:.0f}$ & "
                f"{p.llm_mean:.0f} & {p.rados_mean:.0f} & "
                f"{p.other_mean:.0f} \\\\"
            )
        pr = "\n".join(rows)

        return rf"""
% Table 5: Latency Decomposition
\begin{{table}}[t]
\centering
\caption{{Per-operation latency decomposition (ms, {rpt.iterations} iterations).}}
\label{{tab:latency}}
\begin{{tabular}}{{lrrrr}}
\toprule
\textbf{{Operation}} & \textbf{{Total}} & \textbf{{LLM}} &
  \textbf{{RADOS}} & \textbf{{Other}} \\
\midrule
{pr}
\midrule
\textbf{{Overall}} & ${rpt.overall_mean_ms:.0f}$ &
  \multicolumn{{3}}{{c}}{{P50={rpt.overall_p50_ms:.0f}, P95={rpt.overall_p95_ms:.0f},
  P99={rpt.overall_p99_ms:.0f}}} \\
\bottomrule
\end{{tabular}}
\end{{table}}
"""

    # ── Table 6: CLI comparison ──────────────────────────────────────

    def _latex_cli(self, rpt) -> str:
        rows = []
        for c in rpt.cli_profiles:
            rows.append(
                f"  {c.label.replace('_', ' ').title()} & "
                f"${c.agent_mean:.0f} \\pm {c.agent_std:.0f}$ & "
                f"${c.cli_mean:.0f} \\pm {c.cli_std:.0f}$ & "
                f"{c.overhead_pct:.0f} \\\\"
            )
        cr = "\n".join(rows)

        # averages
        a_avg = statistics.mean([c.agent_mean for c in rpt.cli_profiles]) \
            if rpt.cli_profiles else 0
        c_avg = statistics.mean([c.cli_mean for c in rpt.cli_profiles]) \
            if rpt.cli_profiles else 0
        o_pct = ((a_avg - c_avg) / c_avg * 100) if c_avg > 0 else 0

        return rf"""
% Table 6: Agent vs CLI
\begin{{table}}[t]
\centering
\caption{{Agent vs.\ native CLI latency comparison (ms).}}
\label{{tab:cli}}
\begin{{tabular}}{{lrrr}}
\toprule
\textbf{{Operation}} & \textbf{{Agent (ms)}} & \textbf{{CLI (ms)}} &
  \textbf{{Overhead (\%)}} \\
\midrule
{cr}
\midrule
\textbf{{Average}} & \textbf{{{a_avg:.0f}}} & \textbf{{{c_avg:.0f}}} &
  \textbf{{{o_pct:.0f}}} \\
\bottomrule
\end{{tabular}}
\end{{table}}
"""

    # ==================================================================
    #  Figures (matplotlib)
    # ==================================================================

    def _make_figures(self, intent, react, safety, anomaly, latency):
        if intent:
            self._fig_intent_by_category(intent)
            self._fig_intent_by_difficulty(intent)
        if react:
            self._fig_react_comparison(react)
        if latency:
            self._fig_latency_breakdown(latency)
            if latency.cli_profiles:
                self._fig_cli_comparison(latency)

    # ── Fig 1: Intent accuracy per category ──────────────────────────

    def _fig_intent_by_category(self, rpt):
        cats = sorted(rpt.category_accuracy.keys())
        means = [rpt.category_accuracy[c]["mean"] for c in cats]
        stds = [rpt.category_accuracy[c]["std"] for c in cats]
        labels = [c.replace("_", "\n") for c in cats]

        fig, ax = plt.subplots(figsize=(FULL_WIDTH, 2.2))
        x = range(len(cats))
        ax.bar(x, means, yerr=stds, capsize=3, color="#4C72B0",
               edgecolor="white", linewidth=0.5)
        ax.set_xticks(x)
        ax.set_xticklabels(labels, rotation=45, ha="right")
        ax.set_ylabel("Intent Accuracy (%)")
        ax.set_ylim(0, 105)
        ax.axhline(y=rpt.intent_accuracy_mean, ls="--", color="grey",
                    lw=0.8, label=f"Overall: {rpt.intent_accuracy_mean:.1f}%")
        ax.legend(loc="lower right")
        ax.grid(axis="y", alpha=0.3)
        fig.tight_layout()
        path = self.out / f"fig_intent_category_{self.ts}.pdf"
        fig.savefig(path)
        plt.close(fig)
        logger.info("Saved figure: %s", path)

    # ── Fig 2: Intent accuracy by difficulty ─────────────────────────

    def _fig_intent_by_difficulty(self, rpt):
        diffs = ["easy", "medium", "hard"]
        means = [rpt.difficulty_accuracy.get(d, {}).get("mean", 0)
                 for d in diffs]
        stds = [rpt.difficulty_accuracy.get(d, {}).get("std", 0)
                for d in diffs]

        fig, ax = plt.subplots(figsize=(COL_WIDTH, 2.0))
        ax.bar(diffs, means, yerr=stds, capsize=4,
               color=["#55A868", "#4C72B0", "#C44E52"],
               edgecolor="white", linewidth=0.5)
        ax.set_ylabel("Intent Accuracy (%)")
        ax.set_ylim(0, 105)
        ax.grid(axis="y", alpha=0.3)
        fig.tight_layout()
        path = self.out / f"fig_intent_difficulty_{self.ts}.pdf"
        fig.savefig(path)
        plt.close(fig)
        logger.info("Saved figure: %s", path)

    # ── Fig 3: ReAct vs Simple ───────────────────────────────────────

    def _fig_react_comparison(self, rpt):
        metrics = ["Success Rate\n(%)", "Avg Latency\n(s)"]
        simple_vals = [rpt.simple_success_rate,
                       rpt.simple_avg_latency_ms / 1000]
        react_vals = [rpt.react_success_rate,
                      rpt.react_avg_latency_ms / 1000]

        fig, axes = plt.subplots(1, 2, figsize=(FULL_WIDTH, 2.0))

        # success rate
        ax = axes[0]
        x = [0, 1]
        ax.bar(x, [rpt.simple_success_rate, rpt.react_success_rate],
               color=["#4C72B0", "#C44E52"], edgecolor="white")
        ax.set_xticks(x)
        ax.set_xticklabels(["Simple", "ReAct"])
        ax.set_ylabel("Task Success Rate (%)")
        ax.set_ylim(0, 105)
        ax.grid(axis="y", alpha=0.3)

        # latency
        ax = axes[1]
        ax.bar(x, [rpt.simple_avg_latency_ms / 1000,
                    rpt.react_avg_latency_ms / 1000],
               color=["#4C72B0", "#C44E52"], edgecolor="white")
        ax.set_xticks(x)
        ax.set_xticklabels(["Simple", "ReAct"])
        ax.set_ylabel("Avg Latency (s)")
        ax.grid(axis="y", alpha=0.3)

        fig.tight_layout()
        path = self.out / f"fig_react_comparison_{self.ts}.pdf"
        fig.savefig(path)
        plt.close(fig)
        logger.info("Saved figure: %s", path)

    # ── Fig 4: Latency decomposition stacked bar ─────────────────────

    def _fig_latency_breakdown(self, rpt):
        labels = [p.label.replace("_", "\n") for p in rpt.profiles]
        llm = [p.llm_mean for p in rpt.profiles]
        emb = [p.embedding_mean for p in rpt.profiles]
        vec = [p.vector_mean for p in rpt.profiles]
        rio = [p.rados_mean for p in rpt.profiles]
        other = [p.other_mean for p in rpt.profiles]

        fig, ax = plt.subplots(figsize=(FULL_WIDTH, 2.5))
        x = range(len(labels))
        bottom = [0.0] * len(labels)

        colors = ["#4C72B0", "#55A868", "#C44E52", "#8172B2", "#CCB974"]
        names = ["LLM Inference", "Embedding", "Vector Search",
                 "RADOS I/O", "Other"]
        datasets = [llm, emb, vec, rio, other]

        for vals, color, name in zip(datasets, colors, names):
            ax.bar(x, vals, bottom=bottom, color=color, label=name,
                   edgecolor="white", linewidth=0.3)
            bottom = [b + v for b, v in zip(bottom, vals)]

        ax.set_xticks(x)
        ax.set_xticklabels(labels, rotation=45, ha="right")
        ax.set_ylabel("Latency (ms)")
        ax.legend(loc="upper right", ncol=2, fontsize=FONT_SIZE - 2)
        ax.grid(axis="y", alpha=0.3)
        fig.tight_layout()
        path = self.out / f"fig_latency_breakdown_{self.ts}.pdf"
        fig.savefig(path)
        plt.close(fig)
        logger.info("Saved figure: %s", path)

    # ── Fig 5: Agent vs CLI ──────────────────────────────────────────

    def _fig_cli_comparison(self, rpt):
        labels = [c.label.replace("_", "\n") for c in rpt.cli_profiles]
        agent = [c.agent_mean for c in rpt.cli_profiles]
        cli = [c.cli_mean for c in rpt.cli_profiles]

        fig, ax = plt.subplots(figsize=(FULL_WIDTH, 2.2))
        x = range(len(labels))
        w = 0.35
        ax.bar([i - w / 2 for i in x], agent, w, label="Ceph-SRE Agent",
               color="#4C72B0", edgecolor="white")
        ax.bar([i + w / 2 for i in x], cli, w, label="Native CLI",
               color="#55A868", edgecolor="white")
        ax.set_xticks(x)
        ax.set_xticklabels(labels, rotation=45, ha="right")
        ax.set_ylabel("Latency (ms)")
        ax.set_yscale("log")
        ax.legend()
        ax.grid(axis="y", alpha=0.3)
        fig.tight_layout()
        path = self.out / f"fig_cli_comparison_{self.ts}.pdf"
        fig.savefig(path)
        plt.close(fig)
        logger.info("Saved figure: %s", path)

    # ==================================================================
    #  Text summary
    # ==================================================================

    def _text_summary(self, intent, react, safety, anomaly, latency) -> str:
        lines = [
            "=" * 70,
            "CEPH-SRE  —  CHEOPS 2026 EVALUATION SUMMARY",
            "=" * 70,
            f"Generated: {self.ts}",
            "",
        ]

        if intent:
            lines += [
                "§1  INTENT CLASSIFICATION",
                "-" * 50,
                f"  Model:              {intent.model}",
                f"  Tests per run:      {intent.num_tests_per_run}",
                f"  Runs:               {intent.num_runs}",
                f"  Intent Accuracy:    {intent.intent_accuracy_mean:.1f}% "
                f"± {intent.intent_accuracy_std:.1f}%",
                f"  Param Accuracy:     {intent.param_accuracy_mean:.1f}% "
                f"± {intent.param_accuracy_std:.1f}%",
                f"  Avg Latency:        {intent.avg_latency_mean:.0f} ms "
                f"± {intent.avg_latency_std:.0f} ms",
                "",
            ]

        if react:
            lines += [
                "§2  REACT vs SIMPLE MODE",
                "-" * 50,
                f"  Scenarios:          {react.num_tests}",
                f"  Routing Accuracy:   {react.routing_accuracy:.1f}%",
                f"  Simple Success:     {react.simple_success_rate:.1f}%",
                f"  ReAct Success:      {react.react_success_rate:.1f}%",
                f"  Simple Latency:     {react.simple_avg_latency_ms:.0f} ms",
                f"  ReAct Latency:      {react.react_avg_latency_ms:.0f} ms",
                f"  ReAct Avg Steps:    {react.react_avg_steps:.1f}",
                f"  ReAct Tool Covg:    {react.react_tool_coverage:.0%}",
                f"  Wins (R/S/T):       {react.react_wins}/{react.simple_wins}/{react.ties}",
                "",
            ]

        if safety:
            lines += [
                "§3  SAFETY FRAMEWORK",
                "-" * 50,
                f"  Test cases:         {safety.num_tests}",
                f"  Risk Accuracy:      {safety.risk_accuracy:.1f}%",
                f"  Confirm Accuracy:   {safety.confirmation_accuracy:.1f}%",
                f"  Rate-limit OK:      {safety.rate_limit_correct}",
                "",
            ]

        if anomaly:
            lines += [
                "§4  ANOMALY DETECTION",
                "-" * 50,
                f"  Scenarios:          {anomaly.num_scenarios}",
                f"  Score Accuracy:     {anomaly.score_accuracy:.1f}%",
                f"  Macro Precision:    {anomaly.macro_precision:.3f}",
                f"  Macro Recall:       {anomaly.macro_recall:.3f}",
                f"  F1:                 {anomaly.f1_score:.3f}",
                "",
            ]

        if latency:
            lines += [
                "§5  LATENCY PROFILE",
                "-" * 50,
                f"  Operations:         {latency.num_operations}",
                f"  Iterations:         {latency.iterations}",
                f"  Overall Mean:       {latency.overall_mean_ms:.0f} ms",
                f"  Overall P50:        {latency.overall_p50_ms:.0f} ms",
                f"  Overall P95:        {latency.overall_p95_ms:.0f} ms",
                f"  Overall P99:        {latency.overall_p99_ms:.0f} ms",
                "",
            ]
            if latency.cli_profiles:
                a_avg = statistics.mean(
                    [c.agent_mean for c in latency.cli_profiles])
                c_avg = statistics.mean(
                    [c.cli_mean for c in latency.cli_profiles])
                o_pct = ((a_avg - c_avg) / c_avg * 100) if c_avg > 0 else 0
                lines += [
                    "  CLI COMPARISON",
                    f"    Agent avg:        {a_avg:.0f} ms",
                    f"    CLI avg:          {c_avg:.0f} ms",
                    f"    Overhead:         {o_pct:.0f}%",
                    "",
                ]

        lines.append("=" * 70)
        return "\n".join(lines)
